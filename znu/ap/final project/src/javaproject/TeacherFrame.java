    public void Insert() {
        dtm = new DefaultTableModel(0, 0);
        dtm.setColumnIdentifiers(header);
        jTable1.setModel(dtm);
        for (int i = 0; i < ListTeacher.size(); i++) {
            if (ListTeacher.get(i).getID().equals(ID) == true)
             for (int j = 0; j < ListTeacher.get(i).ListClass.size(); j++)
            {
                dtm.addRow(new Object[]{ListTeacher.get(i).ListClass.get(j).getDateTime(), ListTeacher.get(i).ListClass.get(j).getVahed(), ListTeacher.get(i).ListClass.get(j).getName()});

            }
        }

    }

